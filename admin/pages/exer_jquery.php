<section id="deuxieme">
    <p>
        BMW, acronyme de Bayerische Motoren Werke, est une marque automobile allemande fondée le 7 mars 1916 à Munich.
        Initialement, BMW était spécialisée dans la fabrication de moteurs d'avion. Ce n'est qu'en 1928 que l'entreprise
        commence à produire des automobiles, devenant rapidement une icône dans l'industrie automobile.
    </p>

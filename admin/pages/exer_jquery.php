<section id="quatrieme">
    Entre les années 1970 et 1990, BMW continue d'innover avec des modèles emblématiques tels que la Série 3, la Série 5,
    et la Série 7. La marque se diversifie également en introduisant des SUV avec la gamme X. Ces véhicules rencontrent un
    immense succès et contribuent à l'expansion internationale de BMW. Aujourd'hui, BMW est reconnue pour son engagement
    envers l'électromobilité avec des modèles comme la BMW i3 et la BMW i8, pionniers de la mobilité durable.

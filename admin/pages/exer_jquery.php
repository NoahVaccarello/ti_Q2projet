<section id="troisieme">
    <p>
        L'innovation est au cœur de l'histoire de BMW. Dès ses débuts, la marque a su se distinguer par son engagement
        envers la performance et la technologie de pointe. Les années 1930 voient BMW lancer des modèles emblématiques,
        tels que la BMW 328, qui remporte de nombreuses courses et établit de nouveaux standards en matière de design
        et de performance.
    </p>
